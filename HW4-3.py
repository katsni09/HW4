# 3. Користувач вводить з клавіатури рядок, слово для пошуку, слово для заміни.
# Зробіть у рядку заміну одного слова на інше. Отриманий рядок на екрані.

line = input()
word = input()
word2 = input()
# Метод replace заменяет все вхождения одной строки на другую. Формат: S.replace(old, new)
# — заменить в строке S все вхождения подстроки old на подстроку new.
print(line.replace(word, word2))
